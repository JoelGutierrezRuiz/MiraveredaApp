package com.example.miraveredaapp.model;

public class Actor {
    private int id;
    private String nombre;
    private String personaje;
    private String img;

    public Actor(int id, String nombre, String personaje, String img) {
        this.id = id;
        this.nombre = nombre;
        this.personaje = personaje;
        this.img = img;
    }
}
